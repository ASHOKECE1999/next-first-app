const navLinks = [
  { name: "Register", href: "/register" },
  { name: "Login", href: "/login" },
  { name: "Forgot Password", href: "/forget-password" },
];

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const pathname = usePathname();
  console.log("Current Pathname:", pathname);
  const [userInput, setUserInput] = React.useState("");

  return (
    <div>
      <div>
        <input
          type="text"
          value={userInput}
          onChange={(e) => setUserInput(e.target.value)}
          className="bg-amber-300 outline-0 rounded-3xl p-2"
        />

        {navLinks.map((link) => {
          const isActive =
            pathname === link.href ||
            (pathname.startsWith(link.href) && link.href !== "/");
          console.log(`Checking link: ${link.href}, isActive: ${isActive}`);
          return (
            <Link
              key={link.href}
              href={link.href}
              className={`mr-4 ${isActive ? "font-bold text-blue-600 " : "text-black"}`}
            >
              {link.name}
            </Link>
          );
        })}
      </div>
      {children}
    </div>
  );
}
